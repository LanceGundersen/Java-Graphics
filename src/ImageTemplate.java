    BufferedImage getImage(int[][] data) {
        final int IMGSIZE_X = 25;
        final int IMGSIZE_Y = 25;
        BufferedImage image = new BufferedImage(IMGSIZE_X, IMGSIZE_Y,
                BufferedImage.TYPE_INT_RGB);
        for (int x = 0; x < IMGSIZE_X; x++) {
            for (int y = 0; y < IMGSIZE_Y; y++) {
                int pixelColor = data[x][y];
                if (pixelColor == 0) {
                    pixelColor = Color.WHITE.getRGB();
                } else {
                    pixelColor = Color.RED.getRGB();
                }
                image.setRGB(x, y, pixelColor);
            }
        }
        return image;
    }

